package pe.edu.upeu.modelo;

public class RegistroClienteTO {
    public String idDetRegistro;
    public int npersonas;
    public String dni;
    public String registrofecha;
    public String origen;
    public double precio;
    public int camas;
    public String Habitaciones;
    
    public String getIdDetRegistro() {
        return idDetRegistro;
    }
    public void setIdDetRegistro(String idDetRegistro) {
        this.idDetRegistro = idDetRegistro;
    }
    public int getNpersonas() {
        return npersonas;
    }
    public void setNpersonas(int npersonas) {
        this.npersonas = npersonas;
    }
    public String getDni() {
        return dni;
    }
    public void setDni(String dni) {
        this.dni = dni;
    }
    public String getRegistrofecha() {
        return registrofecha;
    }
    public void setRegistrofecha(String registrofecha) {
        this.registrofecha = registrofecha;
    }
    public String getOrigen() {
        return origen;
    }
    public void setOrigen(String origen) {
        this.origen = origen;
    }
    public double getPrecio() {
        return precio;
    }
    public void setPrecio(double precio) {
        this.precio = precio;
    }
    public int getCamas() {
        return camas;
    }
    public void setCamas(int camas) {
        this.camas = camas;
    }
    public String getHabitaciones() {
        return Habitaciones;
    }
    public void setHabitaciones(String habitaciones) {
        Habitaciones = habitaciones;
    }




    
    
    



    
    
}
